package CheatSheet.CompetitveCoding;

/**
 * The List node.
 */
public class ListNode {
    /**
     * The Val.
     */
    int val;
    /**
     * The Next.
     */
    ListNode next;

    /**
     * Instantiates a new List node.
     *
     * @param x the x
     */
    ListNode(int x) {
        val = x;
    }
}
